// A TanStack Start server middleware that verifies a Supabase JWT from the
// incoming HTTP request's Authorization header and passes the authenticated
// user's ID and claims downstream to server functions.
//
// How it works:
// 1. Reads the `Authorization: Bearer <token>` header from the request.
// 2. Creates a per-request Supabase client with the token attached (so any
//    subsequent DB queries run with that user's identity and obey RLS policies).
// 3. Validates the token using `getClaims` — this extracts the JWT claims
//    without making a network call to Supabase Auth.
// 4. Makes `supabase`, `userId`, and `claims` available to the next handler.
//
// Usage in a server function:
//   import { requireSupabaseAuth } from "@/integrations/supabase/auth-middleware";
//   const myFn = createServerFn()
//     .middleware([requireSupabaseAuth])
//     .handler(async ({ context }) => {
//       const { userId } = context; // type-safe, already verified
//     });
//
// KEYWORDS: AUTH, MIDDLEWARE
// =============================================================================

